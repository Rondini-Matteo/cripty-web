        <div class="content-section-heading">
          <h2 class="mb-5" style="color:#ffd480;">General Data Protection Regulation (GDPR)</h2>
        </div>
        <div>
			<div class="testo mx-auto">
			<p class="lead mb-5" style="font-size:2vw; ">Il 25 maggio 2018 è il giorno in cui il nuovo Regolamento UE 2016/<strong>679</strong> è stato applicato in tutti i 
Paesi dell’Unione Europea ed ha sostituito l’attuale Codice della Privacy. Il Regolamento introduce regole più chiare e semplici in materia di 
informativa e consenso, per garantire maggiori tutele per i cittadini. In Italia questo ruolo sarà gestito dal Garante della Privacy. 
Entra in vigore l’obbligo di segnalazione per i casi di violazione dei dati personali (data breach).
L’<strong>informativa</strong> è resa in forma trasparente, facilmente accessibile e con un linguaggio semplice e chiaro.
Per il <strong>consenso</strong>, è valida qualsiasi manifestazione di volontà con la quale l’interessato accetta, attraverso una dichiarazione che i dati 
personali che lo riguardano siano oggetto di trattamento. Il consenso potrà essere revocato in ogni momento.
Viene introdotto il cosiddetto «<strong>diritto all’oblio</strong>»: il diritto da parte di un interessato ad ottenere la cancellazione dei propri dati personali
da parte del titolare del trattamento, qualora i dati non siano più necessari per gli scopi rispetto ai quali sono stati raccolti, 
oppure se i dati sono trattati illegalmente o se l’interessato si oppone legalmente al loro trattamento.
Il <strong>Data Protection Officer</strong>, abbreviato in DPO, rappresenta una nuova figura nel panorama italiano introdotta da questo Regolamento 679.
 </p>
