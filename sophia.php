	<!-- Cos'è un Malware -->
    <section class="content-section bg-primary text-white text-center" id="malware">
      <div>
        <div class="content-section-heading">
          <h2 class="mb-5" style="color:#ffd480;">Cos’è un Malware?</h2>
        </div>
        <div>
			<div class="testo mx-auto">
				<p class="lead mb-5" style="font-size:2vw; ">Un Malware (“software maligno”) è un qualsiasi programma creato per effettuare un’azione non autorizzata 
			che può danneggiare il funzionamento e la sicurezza del sistema operativo.</p>
			<ul class="lead mb-5" style="font-size:2vw; ">
	<li><strong>Virus</strong>:è un programma che si propaga di file in file all’interno di uno stesso PC, muovendosi poi da un PC all’altro.
	Può essere programmato per cancellare o danneggiare dati.
</li>
	<li><strong>Worm</strong>: utilizzano la rete per propagarsi, possono provocare danni diretti o danni indiretti.
</li>
	<li><strong>Trojan</strong>: appare come software legittimo ma che, una volta lanciato, danneggia il computer.
</li>
	<li><strong>Phishing</strong>: è una tecnica finalizzata a ottenere dati personali sensibili attraverso truffe online.
</li>
	<li><strong>Keylogger</strong>: si tratta di programmi che registrano tutto ciò che l’utente digita sulla tastiera.
</li>
	<li><strong>Botnet</strong>: è una rete di computer collegati ad internet, infettati con un malware e controllati a distanza dai cyber-criminali.
</li>
	<li><strong>Ransomware</strong>: limita l'accesso del dispositivo che infetta, richiedendo un riscatto da pagare per rimuovere la limitazione. 
</li>
	</ul>
	</div>
		</div>
        </div>
      </div>
    </section>   

 
	
