		<div class="row">
			<div class="testo mx-auto">
<p class="lead mb-5" style="font-size:2vw;">La security aziendale non può oggi trascurare smartphone e dispositivi mobili. Con tante nuove applicazioni 
introdotte giornalmente, non è più possibile chiudere gli occhi su cosa impiegati e collaboratori usano sui loro smartphone per scopi aziendali.
Le aziende hanno difficoltà nel decidere quali applicazioni siano o meno adatte e sicure per l’utilizzo nel business.
Tramite la valutazione delle applicazioni (application assessment) i team di sicurezza possono comprendere cosa un’applicazione fa e 
come interagisce con gli altri dati presenti sui dispositivi mobili; potendo così determinare se un’app è adatta o meno all’uso aziendale ed i suoi rischi. 
</p>
<p class="lead mb-5" style="font-size:2vw;">Come fare <strong>application assessment</strong>:</p>
<ul class="lead mb-5" style="font-size:2vw;">
<li>Identificare i comportamenti pericolosi da cui difendersi.</li>
<li>Ispezionare ciascuna applicazione per apprendere tutte le sue funzioni. Prendere poi la decisione se è adatta o meno per l’utilizzo in ambito aziendale.</li>
</ul>
